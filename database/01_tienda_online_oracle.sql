-- Limpieza opcional
BEGIN
    EXECUTE IMMEDIATE 'DROP TABLE COMPRAS';
EXCEPTION
    WHEN OTHERS THEN NULL;
END;
/
BEGIN
    EXECUTE IMMEDIATE 'DROP TABLE PRODUCTOS';
EXCEPTION
    WHEN OTHERS THEN NULL;
END;
/
BEGIN
    EXECUTE IMMEDIATE 'DROP TABLE USUARIOS';
EXCEPTION
    WHEN OTHERS THEN NULL;
END;
/

CREATE TABLE USUARIOS (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NOMBRE_COMPLETO VARCHAR2(120) NOT NULL,
    CORREO VARCHAR2(120) NOT NULL UNIQUE,
    PASSWORD VARCHAR2(120) NOT NULL,
    ROL VARCHAR2(20) NOT NULL,
    ACTIVO NUMBER(1) DEFAULT 1 NOT NULL
);

CREATE TABLE PRODUCTOS (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NOMBRE VARCHAR2(120) NOT NULL,
    DESCRIPCION VARCHAR2(300) NOT NULL,
    CATEGORIA VARCHAR2(80) NOT NULL,
    PRECIO NUMBER(10,2) NOT NULL,
    STOCK NUMBER(10) NOT NULL,
    ACTIVO NUMBER(1) DEFAULT 1 NOT NULL
);

CREATE TABLE COMPRAS (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    PRODUCTO_ID NUMBER NOT NULL,
    NOMBRE_PRODUCTO VARCHAR2(120) NOT NULL,
    CLIENTE_NOMBRE VARCHAR2(120) NOT NULL,
    CLIENTE_CORREO VARCHAR2(120) NOT NULL,
    CANTIDAD NUMBER(10) NOT NULL,
    TOTAL NUMBER(10,2) NOT NULL,
    FECHA_COMPRA TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL
);

